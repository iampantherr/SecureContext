// One-shot extractor: reads A2A_dispatcher/roles.json, writes
// src/skills/roles_catalog.ts with just {name, desc} pairs (no deepPrompts).
//
// Usage:
//   node scripts/sync-roles-from-dispatcher.mjs [path/to/roles.json]
//
// Re-run when the dispatcher's role list changes.

import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const inputPath = process.argv[2] ?? resolve(process.cwd(), "..", "A2A_dispatcher", "roles.json");
const outputPath = resolve(process.cwd(), "src", "skills", "roles_catalog.ts");

const raw = readFileSync(inputPath, "utf8");
const roles = JSON.parse(raw);

const entries = Object.entries(roles).map(([name, def]) => ({
  name,
  desc: def.desc ?? "",
}));

const ts = `// Auto-generated by scripts/sync-roles-from-dispatcher.mjs
// Source: A2A_dispatcher/roles.json (${entries.length} roles)
// Generated: ${new Date().toISOString()}
//
// DO NOT EDIT BY HAND. Re-run the sync script when the dispatcher's role
// list changes:
//   node scripts/sync-roles-from-dispatcher.mjs

export interface RoleEntry {
  name: string;
  desc: string;
}

export const ROLES_CATALOG: RoleEntry[] = ${JSON.stringify(entries, null, 2)};

export const ROLE_NAMES: Set<string> = new Set(ROLES_CATALOG.map((r) => r.name));
`;

writeFileSync(outputPath, ts, "utf8");
console.log(`Wrote ${entries.length} roles to ${outputPath}`);
